        public static int[] getLetter(int number, int[][][] data) { //funkcja zwarca pojedyńczą literę z zestawu danych jako tablicę
            int[] result = new int[nFields];
            for (int i = 0; i < numberY; i++) {
                for (int j = 0; j < numberX; j++) {
                    result[i * numberX + j] = data[number][i][j];
                }
            }
            return result;
        }
